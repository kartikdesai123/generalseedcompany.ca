                                    <p>A fine leafed grass that is drought tolerant, low maintenance, and withstands the shade. Extremely winter hardy. Under ideal conditions it can take 20 days to germinate.</p>
                                    <h3>Chewing Fescue:</h3>
                                    <p>A fine leafed fescue that likes shady areas and low fertility soils, chewing fescue is aggressive and resistant to many diseases.</p>
                                    <h3>Tall Fescue:</h3>
                                    <p>Tall Fescue has a slightly wider blade than other fescues. Tall Fescue tolerates heat and drought exceptionally well because of its deep root system.</p>
