                                        <p>2. Make sure you have a fine smooth seed bed. Remove all stones, lumps and other debris.</p>
                                        <p>3. Apply starter fertilizer at the recommended rate.</p>
                                        <p>4. Apply the seed evenly (approximately 5 to 7 lbs per 1000 sq.ft.) If using a spreader, apply half going horizontally and the other half going vertically.</p>
                                        <p>5. Rake or roll the soil lightly after applying the seed.</p>
                                        <p>6. Keep the area moist by frequently light watering until the seed is established.</p>
                                        <p>7. Depending on the grass species, under ideal growing conditions, some seed will germinate in one week, while others will take four weeks.</p>
                                        <p>8. Seed germinates best when temperatures are 15̊ - 26̊ C. Seed applied at cooler temperatures may take longer to emerge.</p>
                                        <p>9. For best results, apply in early to mid-spring or early fall.</p>
