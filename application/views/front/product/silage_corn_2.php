                                <span class="heading display-block">Leafy’s Have HigherTotal Plant Digestibility</span>
                                <div class="clear"></div>
                                <span class="heading">The Digestible Stalk</span>
                                <p>
                                    Leafies have been selected to have stalks that are composed of an appropriate
                                    balance of effective and digestible fiber for the silage ration. When examining
                                    the digestibility of the corn stalk, we know that the stalk below the ear
                                    consists of more non-digestible heavily lignifled fiber that is needed to support
                                    the weight of the ear. Leafy hybrids have been selected to have a lower
                                    ear position on the plant to reduce the amount of overall plant lignin and to
                                    increase the digestible fiber found above the ear. The total stalk is strong
                                    enough to reach silage maturity. Compare that to a grain hybrid that is bred
                                    to have higher ears for combining and stronger more lignin-filled stalks to last
                                    weeks or months beyond silage harvest. It just makes sense that Leafles
                                    offer a better balance of effective and digestible fiber for silage feeding.
                                </p>
                                <span class="heading">The Digestible Ear</span>
                                <p>
                                    Leafles have been bred to have large ears. Unlike the ears of grain varieties,
                                    a Leafy ear is composed of large kernels that dry slowly on large digestible
                                    cobs. The kernels have lower test weight if taken to grain maturity because
                                    they are composed of more floury white starch and less vitreous starch. The
                                    large, moist, more floury kernels break into smaller pieces during harvest
                                    processing. This smaller particle size allows more sites of digestion for
                                    rumen microbes. Combined with higher kernel moisture, the silage can be
                                    economically fed right after fermentation.
                                </p>
