                            <div class="left-content">
                                <span class="heading display-block">Leafy’s Are Reliable and Offer Added Crop Security</span>
                                <div class="clear"></div>
                                <span class="heading">Harvest Window</span>
                                <p>
                                    Leafy hybrids have been bred for an extended silage harvest window. The
                                    Leaty plant is designed to dry down at a rate that compliments the ear, while
                                    maintaining plant health. Leafy ears have slow drying kernels that remain soft
                                    through the ideal whole plant silage moisture range of 62-68%. During a study
                                    at the University of Wisconsin, researchers harvested plots of a Leafy and a
                                    Dual Purpose hybrid at as close to 65% moisture as possible. They then left
                                    a couple of rows of each variety standing in the field and returned weekly to
                                    test kernel moisture. After three weeks, the Leafy still had a kernel moisture of
                                    30%. This slow kernel drydown helps preserve starch digestibility.
                                </p>
                                <span class="heading">Population</span>
                                <p>
                                    Leafies perform better when planted at 28,000 to 30,000 plants per acre
                                    (ppa). Our population studies have shown that Leafies planted at 28,000
                                    ppa have thicker stalks and bigger ears than Leafies planted at 35,000 ppa.
                                    The thicker stalks are more digestible and the bigger ears make a silage that
                                    contains a higher percentage of overall starch.
                                </p>
                                <p>
                                    It is essential to grow Leafies at their intended populations. Increasing density
                                    can alter flowering dates and maturity, drought response, standability and
                                    overall plant composition, all of which affect the feeding value of the silage
                                    product.
                                </p>
                                <p>
                                    For the best crop security and yield, grow Leafy hybrids at 28,000 ppa.
                                </p>
