                            <div class="right-content">
                                <span class="heading display-block">Leafy Floury Hybrids - The Next Step in Corn Silage</span>
                                <div class="clear"></div>
                                <!--<span class="heading">Test2 Test2</span>-->
                                <p>
                                    Leafy Floury hybrids feature the same silage-specific characteristics
                                    as Leafy hybrids with the addition of more ruminally-available starch
                                    for milk production. This bump in floury starch is achieved by the
                                    presence of a naturally occurring recessive gene called opaque-i.
                                    Since the opaque-i gene is recessive, Leafy Floury hybrids produce
                                    25% full-floury kernels. The remaining 75% are typical of Leafy’s soft,
                                    slow-drying digestible starch.
                                </p>
                                <span class="heading">Smaller Particle Size</span>
                                <p>
                                    Floury kernels are softer and fracture into finer particles during silage
                                    harvest. This fine particle size translates into higher quantities of
                                    available starch right after fermentation is complete and allows the
                                    silage to be fed more rapidly with less dry matter loss. Leafy Floury
                                    hybrids can be fed soon after chopping and some dairies have fed a
                                    Leafy Floury ration immediately with no milk slump when compared
                                    to the ration of year-old silage.
                                </p>
                                <span class="heading">More Starch Digestibility</span>
                                <p>
                                    Seven hour starch tests have revealed that Leafy Floury hybrids have
                                    5-1 2% more starch digestibility than a regular Leafy hybrid. A 7-hour
                                    starch test measures ruminally-available starch, and a higher rate of
                                    starch disappearance is thought to indicate rumen starch conversion
                                    to milk production, rather than to body maintenance. Leafy Floury
                                    hybrids also have higher 2-hour and 4-hour starch disappearance
                                    values.
                                </p>
                                <p>
                                    We recommend that a 7-hr starch analysis be run on silage as an
                                    indication of feed value. Farmers who have used Leafy Floury hybrids
                                    have told us they are able to reduce the quantity of concentrate corn
                                    in the total mixed ration and still maintain milk production and milk
                                    component levels.
                                </p>
